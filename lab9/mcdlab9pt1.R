mydirichlet <- function(a=c(1,1,1), k=1){
	DIRI <- matrix(rgamma(length(a)*k, a, 1), 
		nrow=k, ncol=length(a), byrow=T)
	DIRI/apply(DIRI,1,sum)
}

alpha <- c(6,1,6)
z <- mydirichlet(a=alpha, k=10)
print(z)

